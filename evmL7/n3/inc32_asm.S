# Функция inc32_asm: инкремент 32-битного значения по указателю p
.global inc32_asm
inc32_asm:
    # Вход:
    #   rdi - void* p (адрес 32-битного значения)
    # Действие:
    #   inc dword ptr [rdi] (инкремент 32-битного значения)
    # Выход:
    #   значение по адресу p увеличивается на 1
    lock addl $1, (%rdi)  # lock для атомарности (если требуется)
    ret